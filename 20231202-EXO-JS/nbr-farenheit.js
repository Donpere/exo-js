// 5 - Conversion de température :

// Écrivez une fonction qui convertit une température de Celsius en Fahrenheit. La formule est : 
// Fahrenheit = (Celsius * 9/5) + 32.

function Fahrenheit(Celsius) {
    return ((Celsius * 9 / 5) + 32);
}

console.log(Fahrenheit(32));
console.log(Fahrenheit(77));
console.log(Fahrenheit(0));
console.log(Fahrenheit(8));
console.log(Fahrenheit(22));
